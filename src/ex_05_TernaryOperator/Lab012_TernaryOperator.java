package ex_05_TernaryOperator;

public class Lab012_TernaryOperator {
    public static void main(String[] args) {
        // Ternary Operator
        //result = condition ? expression1 : expression2

        int age = 19;
        String canPersonGoToGoa = age > 18 ? "Yes" : "No";
        // If the condition is true then Yes will assign to data type and if condition is false then No is assign to variable
        System.out.println(canPersonGoToGoa);
    }
}
